<h3>Keyboard control</h3>

<h4>Matrix cell navigation</h4>
<div>
  <span class="label">⌘</span> + <span class="label">↑</span>
  Move focus previous row cell
</div>
<div>
  <span class="label">⌘</span> + <span class="label">↓</span>
  Move focus next row cell
</div>
<div>
  <span class="label">⌘</span> + <span class="label">→</span>
  Move focus next column cell
</div>
<div>
  <span class="label">⌘</span> + <span class="label">←</span>
  Move focus previous column cell
</div>

<h4>Number input manipulation</h4>
<div>
  <span class="label">↑</span> - increase value
</div>
<div>
  <span class="label">↓</span> - decrease value
</div>
